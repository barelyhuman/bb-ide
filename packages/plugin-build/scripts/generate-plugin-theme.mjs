// Regenerates src/generated/plugin-theme.generated.ts from the host app's
// Tailwind theme entry (apps/app/src/components/ui/theme.css) and the
// repo-installed tw-animate-css.
//
// The plugin CSS pass (`bb plugin build`) compiles utilities against the same
// semantic tokens the host uses (`bg-background`, `text-sm`, `animate-in`, …).
// Both inputs are embedded as strings so the packaged CLI needs no runtime
// resolution of app source or style-only npm exports (tw-animate-css exports
// only a `style` condition — require.resolve cannot reach it).
//
//   node packages/plugin-build/scripts/generate-plugin-theme.mjs [--check]
//
// `--check` exits 1 when the checked-in file is stale (wired into this
// package's typecheck, same pattern as @bb/templates).
import { readFile, writeFile, mkdir } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const packageRoot = path.join(scriptDir, "..");
const repoRoot = path.join(packageRoot, "..", "..");
const themeCssPath = path.join(
  repoRoot,
  "apps",
  "app",
  "src",
  "components",
  "ui",
  "theme.css",
);
// Read through apps/app's node_modules link so we vendor exactly the version
// the app ships (its package.json exports block require.resolve, so plain
// path reads are the only way in).
const twAnimateDir = path.join(
  repoRoot,
  "apps",
  "app",
  "node_modules",
  "tw-animate-css",
);
const outPath = path.join(
  packageRoot,
  "src",
  "generated",
  "plugin-theme.generated.ts",
);

/**
 * Extract every top-level `@theme … { … }` block verbatim (brace-counted so a
 * nested `@keyframes` inside a theme block cannot truncate the extraction).
 * The `:root` palettes, `@layer` rules, and media overrides around them stay
 * host-only: the host defines those custom properties at runtime and plugin
 * sheets read them live.
 */
function extractThemeBlocks(css) {
  const blocks = [];
  const re = /@theme\b[^{]*\{/g;
  let match;
  while ((match = re.exec(css)) !== null) {
    let depth = 1;
    let i = re.lastIndex;
    while (i < css.length && depth > 0) {
      const ch = css[i];
      if (ch === "{") depth += 1;
      else if (ch === "}") depth -= 1;
      i += 1;
    }
    if (depth !== 0) {
      throw new Error(`unbalanced braces in @theme block at ${match.index}`);
    }
    blocks.push(css.slice(match.index, i));
    re.lastIndex = i;
  }
  if (blocks.length === 0) {
    throw new Error(`no @theme blocks found in ${themeCssPath}`);
  }
  return blocks;
}

function escapeTemplateLiteral(value) {
  return value.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
}

const [themeCss, twAnimateCss, twAnimatePackageJson] = await Promise.all([
  readFile(themeCssPath, "utf8"),
  readFile(path.join(twAnimateDir, "dist", "tw-animate.css"), "utf8"),
  readFile(path.join(twAnimateDir, "package.json"), "utf8"),
]);
const twAnimateVersion = JSON.parse(twAnimatePackageJson).version;
const themeBlocks = extractThemeBlocks(themeCss).join("\n\n");

const generated = `// Generated by scripts/generate-plugin-theme.mjs — do not edit.
// Source of truth: apps/app/src/components/ui/theme.css (+ the repo's
// installed tw-animate-css). Regenerate after either changes:
//
//   node packages/plugin-build/scripts/generate-plugin-theme.mjs

/**
 * The host app's \`@theme\` blocks, verbatim: the semantic-token bridge
 * (\`--color-background: var(--background)\`, radius, fonts, shadows) plus the
 * typography-scale overrides. Values resolve against the host's live CSS
 * variables at runtime, so plugin-compiled utilities track the active theme.
 */
export const PLUGIN_THEME_CSS = \`${escapeTemplateLiteral(themeBlocks)}\`;

/** Vendored tw-animate-css@${twAnimateVersion} (the version apps/app ships). */
export const TW_ANIMATE_CSS = \`${escapeTemplateLiteral(twAnimateCss.trimEnd())}\`;
`;

const check = process.argv.includes("--check");
let existing = null;
try {
  existing = await readFile(outPath, "utf8");
} catch {
  // missing → stale
}

if (check) {
  if (existing !== generated) {
    console.error(
      "plugin-theme.generated.ts is stale. Run: node packages/plugin-build/scripts/generate-plugin-theme.mjs",
    );
    process.exit(1);
  }
} else if (existing !== generated) {
  await mkdir(path.dirname(outPath), { recursive: true });
  await writeFile(outPath, generated);
  console.log(`wrote ${path.relative(repoRoot, outPath)}`);
} else {
  console.log("plugin-theme.generated.ts up to date");
}
